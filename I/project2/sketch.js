function setup() {
	createCanvas(windowWidth, windowHeight);
	background(150);


    fill(75) /* couleur du trais*/
	rectMode(CENTER); /* emplacement du rectangle*/
    rect(width/2, height/2, 500, 500); /* dimensions du rectangle, width/2: centre vertical, height/2: centre horizontal*/ 

	fill(0, 255, 0)

	ellipseMode(CORNER);

	ellipse(width/2, height/2, 50, 50)




}
